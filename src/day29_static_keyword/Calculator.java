package day29_static_keyword;

public class Calculator {
    static double add(double a, double b) {
        return a + b;
    }

    static double subs(double a, double b) {
        return a - b;
    }

    static double mult(double a, double b) {
        return a * b;
    }

    static double div(double a, double b) {
        return a / b;
    }
}


