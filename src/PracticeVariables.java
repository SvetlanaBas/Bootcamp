
public class PracticeVariables {
    public static void main(String[] args) {
        byte firstByteValue = 25;
        byte secondByteValue = -115;
        byte thirdByteValue = 127;

        short firstShortValue = -30000;
        short secondShortValue = 0;
        short thirdShortValue = 32767;

        int firstIntValue = -265165156;
        int secondIntValue = 65165;
        int thirdIntValue = 951295151;

        long firstLongValue = -154156451L;
        long secondLongValue = 0L;
        long thirdLongValue = 155616512248L;

        float firstFloatValue = -415.1211f;
        float secondFloatValue = 12.6455454f;
        float thirdFloatValue = 99.951442f;

        double firstDoubleValue = -21231.2222222222222222;
        double secondDoubleValue = 6152.66666666666666666;
        double thirdDoubleValue = 9.965415616132132;

        boolean firstBooleanValue = true;
        boolean secondBooleanValue = false;

        char firstCharValue = 'x';
        char secondCharValue = 'y';
        char thirdCharValue = 'z';

        System.out.println("Bytes: " + firstByteValue + ", " + secondByteValue + ", " + thirdByteValue);
        System.out.println("Shorts: " + firstShortValue + ", " + secondShortValue+ ", " + thirdShortValue);
        System.out.println("Ints: " + firstIntValue + ", " + secondIntValue + ", " + thirdIntValue);
        System.out.println("Longs: " + firstLongValue + ", " + secondLongValue + ", " + thirdLongValue);
        System.out.println("Floats: " + firstFloatValue + ", " + secondFloatValue + ", " + thirdFloatValue);
        System.out.println("Doubles: " + firstDoubleValue + ", " + secondDoubleValue + ", " + thirdDoubleValue);
        System.out.println("Booleans: " + firstBooleanValue + ", " + secondBooleanValue);
        System.out.println("Chars: " + firstCharValue + ", " + secondCharValue);
    }
}